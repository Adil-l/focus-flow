type TranslationDictionary = typeof translations;
type SupportedLanguage = keyof TranslationDictionary;
type TranslationKey = keyof TranslationDictionary['en'];

export const useTranslation = () => {
  const { language, setLanguage } = useLanguage();
  const currentLanguage = (language in translations ? language : 'en') as SupportedLanguage;
  const t: Record<TranslationKey, string> = translations[currentLanguage];

  return { t, language: currentLanguage, setLanguage, currentLanguage };
}
